// scripts/seo/audit-variant-similarity.mjs
//
// Per playbook §0.3 — Fix 0.3.
// Measure how differentiated the 6 URL-variant pages are for the same city.
// Loads the BUILT static HTML from .next/server/app/, strips tags, computes
// Jaccard similarity between every pair of variants per city using a
// 5-gram (shingled) token bag.
//
// Decision rule (per playbook):
//   >80% overlap → consolidate via 301s; Google will dedupe and demote anyway
//   60–80% overlap → invest in differentiation; current pages risky
//   <60% overlap → variant strategy is working
//
// Usage: node scripts/seo/audit-variant-similarity.mjs

import { readFileSync, existsSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const CITIES = ['chennai', 'bangalore', 'coimbatore'];
const VARIANTS = [
    'taxi-service',
    'drop-taxi',
    'one-way-taxi',
    'outstation-cabs',
    'cab-service',
    'call-taxi',
];

const BUILD_DIR = '.next/server/app';

const stripHtml = (html) =>
    html
        .replace(/<script\b[^>]*>[\s\S]*?<\/script>/gi, ' ')
        .replace(/<style\b[^>]*>[\s\S]*?<\/style>/gi, ' ')
        .replace(/<[^>]+>/g, ' ')
        .replace(/&[a-z0-9#]+;/gi, ' ')
        .replace(/\s+/g, ' ')
        .trim()
        .toLowerCase();

// 5-gram shingles → bag of tokens (Jaccard on the set form)
const shingle = (text, n = 5) => {
    const words = text.split(/\s+/).filter(Boolean);
    const set = new Set();
    for (let i = 0; i <= words.length - n; i++) {
        set.add(words.slice(i, i + n).join(' '));
    }
    return set;
};

const jaccard = (a, b) => {
    let intersect = 0;
    for (const x of a) if (b.has(x)) intersect++;
    const union = a.size + b.size - intersect;
    return union === 0 ? 0 : intersect / union;
};

const pages = {};
for (const city of CITIES) {
    for (const variant of VARIANTS) {
        const path = join(BUILD_DIR, `${variant}-in-${city}.html`);
        if (!existsSync(path)) {
            console.error(`MISSING: ${path}`);
            continue;
        }
        const html = readFileSync(path, 'utf8');
        const text = stripHtml(html);
        pages[`${city}/${variant}`] = {
            wordCount: text.split(/\s+/).length,
            shingles: shingle(text),
        };
    }
}

const lines = [
    '# 6-URL Variant Similarity Audit — Fix 0.3',
    '',
    '> Generated by `scripts/seo/audit-variant-similarity.mjs`. Built HTML from `.next/server/app/`.',
    '> Method: strip tags, lowercase, tokenize, 5-gram shingles, Jaccard similarity per pair.',
    '> Decision rule (playbook §0.3):',
    '> - **>80%** overlap → consolidate to 2-3 URLs with 301s',
    '> - **60-80%** overlap → invest in differentiation',
    '> - **<60%** overlap → variant strategy working',
    '',
    `**Generated:** ${new Date().toISOString().slice(0, 10)}`,
    '**Sampled cities:** Chennai (tier 1, Chennai overrides applied), Bangalore (tier 1), Coimbatore (tier 1, Coimbatore overrides applied)',
    '**Sampled variants:** taxi-service, drop-taxi, one-way-taxi, outstation-cabs, cab-service, call-taxi (all 6)',
    '',
    '---',
    '',
];

// Word counts table
lines.push('## Word counts per variant');
lines.push('');
lines.push('| City | taxi-service | drop-taxi | one-way-taxi | outstation-cabs | cab-service | call-taxi |');
lines.push('|---|---:|---:|---:|---:|---:|---:|');
for (const city of CITIES) {
    const row = [city];
    for (const v of VARIANTS) {
        const k = `${city}/${v}`;
        row.push(pages[k]?.wordCount?.toString() ?? '—');
    }
    lines.push(`| ${row.join(' | ')} |`);
}
lines.push('');

// Per-city similarity matrices
for (const city of CITIES) {
    lines.push(`## ${city} — Pairwise Jaccard similarity (%)`);
    lines.push('');
    lines.push(`| | ${VARIANTS.join(' | ')} |`);
    lines.push(`|---|${VARIANTS.map(() => '---:').join('|')}|`);
    for (const v1 of VARIANTS) {
        const row = [v1];
        for (const v2 of VARIANTS) {
            if (v1 === v2) {
                row.push('—');
                continue;
            }
            const a = pages[`${city}/${v1}`]?.shingles;
            const b = pages[`${city}/${v2}`]?.shingles;
            if (!a || !b) {
                row.push('—');
                continue;
            }
            row.push((jaccard(a, b) * 100).toFixed(1));
        }
        lines.push(`| ${row.join(' | ')} |`);
    }
    lines.push('');

    // Per-city average + decision
    const pairs = [];
    for (let i = 0; i < VARIANTS.length; i++) {
        for (let j = i + 1; j < VARIANTS.length; j++) {
            const a = pages[`${city}/${VARIANTS[i]}`]?.shingles;
            const b = pages[`${city}/${VARIANTS[j]}`]?.shingles;
            if (a && b) pairs.push(jaccard(a, b));
        }
    }
    const avg = pairs.length ? (pairs.reduce((s, x) => s + x, 0) / pairs.length) * 100 : 0;
    const max = pairs.length ? Math.max(...pairs) * 100 : 0;
    const min = pairs.length ? Math.min(...pairs) * 100 : 0;
    let verdict;
    if (max > 80) verdict = `❌ **TOO SIMILAR** — pair max ${max.toFixed(1)}% exceeds the 80% consolidation threshold. Recommend 301 consolidation or aggressive differentiation.`;
    else if (avg > 60) verdict = `⚠️  **MARGINAL** — average ${avg.toFixed(1)}% inside the 60-80% band. Some variants need more distinct content.`;
    else verdict = `✅ **DIFFERENTIATED** — average ${avg.toFixed(1)}%, max ${max.toFixed(1)}%. Variant strategy working.`;
    lines.push(`**${city} verdict:** avg ${avg.toFixed(1)}% · min ${min.toFixed(1)}% · max ${max.toFixed(1)}% — ${verdict}`);
    lines.push('');
}

// Cross-city baseline — what does an unrelated-city pair look like?
lines.push('## Cross-city baseline');
lines.push('');
lines.push('For calibration: how similar are different *cities* of the same variant? This isolates template overlap (which IS expected to be high) from city-specific content.');
lines.push('');
lines.push('| Variant | Chennai vs Bangalore | Bangalore vs Coimbatore | Chennai vs Coimbatore |');
lines.push('|---|---:|---:|---:|');
for (const v of VARIANTS) {
    const a = pages[`chennai/${v}`]?.shingles;
    const b = pages[`bangalore/${v}`]?.shingles;
    const c = pages[`coimbatore/${v}`]?.shingles;
    const ab = a && b ? (jaccard(a, b) * 100).toFixed(1) : '—';
    const bc = b && c ? (jaccard(b, c) * 100).toFixed(1) : '—';
    const ac = a && c ? (jaccard(a, c) * 100).toFixed(1) : '—';
    lines.push(`| ${v} | ${ab} | ${bc} | ${ac} |`);
}
lines.push('');
lines.push('Cross-city overlap measures TEMPLATE bleed (header/footer/nav, shared boilerplate, fleet table, FAQ format).');
lines.push('');

// Output
const output = lines.join('\n');
const outputPath = 'data/variant-similarity-audit.md';
writeFileSync(outputPath, output);
console.log(`Wrote audit to ${outputPath}`);
console.log('');
console.log(output);
